return {
    "zbirenbaum/copilot.lua",
    cmd = "Copilot",
    event = "InsertEnter",
    config = function()
        require("copilot").setup({
            suggestion = {
                enabled = true,
                auto_trigger = true, -- Enable suggestions without explicit trigger
                keymap = {
                    accept = "<C-l>", -- Accept Copilot suggestions
                    accept_word = false,
                    accept_line = false,
                    next = "<M-]>",
                    prev = "<M-[>",
                    dismiss = "<C-]>",
                },
            },
            filetypes = {
                text = false, -- disable for .txt files
            },
        })
    end,
}
